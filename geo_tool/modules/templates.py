# ===== 模板注册表 =====
# 将模板类型映射到对应的Prompt模板字符串
_TEMPLATE_REGISTRY: dict[TemplateType, str] = {
    TemplateType.RANKING: RANKING_TEMPLATE,
    TemplateType.REVIEW: REVIEW_TEMPLATE,
    TemplateType.GUIDE: GUIDE_TEMPLATE,
    TemplateType.COMPARISON: COMPARISON_TEMPLATE,
    TemplateType.CASE_STUDY: CASE_STUDY_TEMPLATE,
}


def get_template(template_type: TemplateType) -> str:
    """根据模板类型获取对应的Prompt模板

    Args:
        template_type: GEO内容模板类型（TemplateType枚举值）

    Returns:
        对应模板类型的Prompt模板字符串

    Raises:
        ValueError: 当传入不支持的模板类型时抛出
    """
    template = _TEMPLATE_REGISTRY.get(template_type)
    if template is None:
        valid_types = ", ".join(t.value for t in TemplateType)
        raise ValueError(
            f"不支持的模板类型: {template_type}。"
            f"支持的类型: {valid_types}"
        )
    return template
